class TranslationService {
  private currentLanguage: string = 'en-IN';
  setLanguage(languageCode: string) {
    this.currentLanguage = languageCode;
  }
  getLanguage(): string {
    return this.currentLanguage;
  }
  translate(key: string): string {
    const translation = TRANSLATIONS[key];
    if (!translation) {
      console.warn(`Translation key not found: ${key}`);
      return key;
    }
    const translatedText = translation[this.currentLanguage];
    if (!translatedText) {
      // Fallback to English if translation not available
      return translation['en-IN'] || key;
    }
    return translatedText;
  }
  // Helper method to translate multiple keys at once
  translateMultiple(keys: string[]): { [key: string]: string } {
    const result: { [key: string]: string } = {};
    keys.forEach(key => {
      result[key] = this.translate(key);
    });
    return result;
  }
  // Get all available languages
  getAvailableLanguages() {
    return INDIAN_LANGUAGES;
  }
}
export const translationService = new TranslationService();